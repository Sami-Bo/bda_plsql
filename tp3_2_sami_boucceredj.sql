CREATE OR REPLACE TYPE BODY TEACHER_TYPE AS
 MEMBER FUNCTION NBCOURSES RETURN NUMBER IS 
 NUMBERCOURSES NUMBER;
 BEGIN
    NUMBERCOURSES := COURSES.COUNT;
    RETURN NUMBERCOURSES;
 END;
END;



