-- Partie objet

CREATE TYPE COURSE_TYPE AS OBJECT (
 TITLE VARCHAR2(10), 
 SEMESTER NUMBER(1)
); 

CREATE TYPE COURSES_TYPE AS TABLE OF COURSE_TYPE;

CREATE TYPE TEACHER_TYPE AS OBJECT (
 NAME VARCHAR2(10), 
 GRADE VARCHAR2 (10),
 COURSES COURSES_TYPE
);

CREATE TABLE TEACHER_TABLE OF TEACHER_TYPE NESTED TABLE COURSES STORE AS COURSES_TABLE;

