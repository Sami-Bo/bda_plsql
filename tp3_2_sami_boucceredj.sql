-- Creation de la table COURSE
-- Le TITLE est la clef primaire et est donc NON NULL
-- C'est cette table qui a une clef etrangere car chaque cours depend d'un prof
CREATE TABLE COURSE ( 
 TITLE VARCHAR2(10) NOT NULL, 
 SEMESTER NUMBER(1),
 NAME VARCHAR2 (10),
 CONSTRAINT PK_COURSE PRIMARY KEY (TITLE),
 CONSTRAINT FK_COURSE_TEACHER FOREIGN KEY (NAME) REFERENCES TEACHER(NAME)
);

