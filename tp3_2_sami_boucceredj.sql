-- Creation de la table TEACHER
-- Le NAME est la clef primaire et est donc NON NULL
CREATE TABLE TEACHER ( 
 NAME VARCHAR2(10) NOT NULL, 
 GRADE VARCHAR2 (10),
 CONSTRAINT PK_TEACHER PRIMARY KEY (NAME)
);
 
